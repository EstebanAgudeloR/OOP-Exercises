            apellidoProgramador2 = String.valueOf(txtApellido2.getText());
            EquipoMaratonProgramacion.validarCampo(apellidoProgramador2);
            Programador programador2 = new Programador(nombreProgramador2, apellidoProgramador2);
            equipo.a침adir(programador2);
            
            
            
            nombreProgramador3 = String.valueOf(txtNombre3.getText());
            EquipoMaratonProgramacion.validarCampo(nombreProgramador3);
            apellidoProgramador3 = String.valueOf(txtApellido3.getText());
            EquipoMaratonProgramacion.validarCampo(apellidoProgramador3);
